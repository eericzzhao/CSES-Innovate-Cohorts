# List initialiazation
nums = [10, 20, 30]

# Adding an element to the list
nums.append(70)

# This gives us the first element
nums[0] 

# This gives us the last element
nums[-1]

# Built-in function gives us the sum
sum(nums)

# Length of the list
len(nums)

# We can even create new lists
# with list comprehensions
unknownList = [x * x for x in range(10)]
